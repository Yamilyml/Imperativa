// Crear una función sumar, esta deberá recibir dos parámetros y retornar la
// suma de los mismos.

const suma = (a, b)=> a + b
let resultadoSuma = suma (1, 2)
console.log(resultadoSuma)

// Crear una función restar, la cual deberá recibir dos parámetros y retornar la
// resta del primero menos el segundo.

const restar = (a, b)=> a -b 
let resultadoresta = restar (2, 1)
console.log(resultadoresta)
// 4. Crear una función multiplicar, la misma deberá recibir dos parámetros y
// retornar el resultado de su multiplicación.

const multiplicar = (a, b)=> a * b
let resultadoMultiplicacion = multiplicar (5, 5)
console.log(resultadoMultiplicacion)


// 5. Crear una función dividir, la cual recibirá dos parámetros y retornar el resultado
// de la división del primero sobre el segundo.

const division = (a, b)=> a / b
let resultadoDivision = division (100, 10)
console.log(resultadoDivision)

